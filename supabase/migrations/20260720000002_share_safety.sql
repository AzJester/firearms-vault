create or replace function private.cleanup_expired_shares()
returns integer
language plpgsql
volatile
security definer
set search_path = pg_catalog, pg_temp
as $$
declare v_deleted integer;
begin
  with doomed as (
    select token from public.shares
    where expires_at < clock_timestamp() - interval '1 day'
    order by expires_at asc limit 500
  )
  delete from public.shares using doomed where shares.token = doomed.token;
  get diagnostics v_deleted = row_count;
  delete from public.share_failed_attempts where attempted_at < clock_timestamp() - interval '24 hours';
  return v_deleted;
end;
$$;
revoke all on function private.cleanup_expired_shares() from public, anon, authenticated;

-- Require AAL2 for private media only when the account has opted in to MFA.
drop policy if exists "media_mfa_opt_in" on storage.objects;
create policy "media_mfa_opt_in" on storage.objects
  as restrictive for all to authenticated
  using (bucket_id <> 'media' or private.session_meets_opt_in_mfa())
  with check (bucket_id <> 'media' or private.session_meets_opt_in_mfa());

-- If Supabase Cron is enabled, this named job is idempotently created/updated.
do $$
begin
  if exists (select 1 from pg_catalog.pg_extension where extname = 'pg_cron') then
    perform cron.schedule(
      'firearms-vault-expired-share-cleanup',
      '17 4 * * *',
      'select private.cleanup_expired_shares()'
    );
  end if;
exception
  when undefined_function or invalid_schema_name or insufficient_privilege then
    raise notice 'Supabase Cron is not enabled; schedule private.cleanup_expired_shares() after enabling it.';
end;
$$;

